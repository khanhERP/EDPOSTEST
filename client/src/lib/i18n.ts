export function useTranslation() {
  const { currentLanguage } = useLanguageStore();

  const t = (key: string): string => {
    const keys = key.split('.');
    let value: any = translations[currentLanguage];
    
    for (const k of keys) {
      value = value?.[k];
    }
    

    
    return value || key;
  };

  return { t, currentLanguage };
}